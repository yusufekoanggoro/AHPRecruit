    private void buttonAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAddMouseClicked
        // TODO add your handling code here:
        try{
            if(!textFieldId.getText().equals("")){ 
            }else if(!textFieldName.getText().equals("")
                && btnG.getSelection() != null
                && cbPendidikan.getSelectedIndex() != 0
                && !textFieldAddress.getText().equals("")
                && !textFieldPhoneNumber.getText().equals("")
                && !textFieldLeadershipScore.getText().equals("")
                && !textFieldKnowledgeScore.getText().equals("") 
                && !textFieldTechnicalSkillScore.getText().equals("")
                && !textFieldAdvancedSkillScore.getText().equals("")
            ){
                CandidateModel newCandidate = new CandidateModel();
                newCandidate.setName(textFieldName.getText());
            
                String gender;
                if(rbLaki.isSelected()){
                    gender = "Laki-Laki";
                }else{
                    gender = "Perempuan";
                }

                newCandidate.setGender(gender);
                newCandidate.setLastEducation(cbPendidikan.getSelectedItem().toString());
                newCandidate.setPhoneNumber(textFieldPhoneNumber.getText());
                newCandidate.setAddress(textFieldAddress.getText());
                newCandidate.setLeadershipScore(Integer.parseInt(textFieldLeadershipScore.getText()));
                newCandidate.setKnowledgeScore(Integer.parseInt(textFieldKnowledgeScore.getText()));
                newCandidate.setTechnicalSkillScore(Integer.parseInt(textFieldTechnicalSkillScore.getText()));
                newCandidate.setAdvancedSkillScore(Integer.parseInt(textFieldAdvancedSkillScore.getText()));

                candidateDao.insertOne(newCandidate);
                
                this.clearForm();
                
                this.dispose();
            }
            else {
                IsiKosong.showMessageDialog(rootPane, "Mohon isi semua kolom isian pada form !", "Error", ERROR_MESSAGE );
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Gagal Menambah Data");
        }
    }//GEN-LAST:event_buttonAddMouseClicked

