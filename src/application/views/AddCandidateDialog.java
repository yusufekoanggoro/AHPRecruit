    public void setDataTabel(String id, String name, String gender, String lastEducation, String phoneNumber, String address, String leadershipScore, String knowledgeScore, String technicalSkillScore, String advancedSkillScore){
        textFieldId.setText(id);
        textFieldName.setText(name);
        if(gender.equals("Laki-Laki")){
            rbLaki.setSelected(true);
            rbPerempuan.setSelected(false);
        }else if(gender.equals("Perempuan")){
            rbLaki.setSelected(false);
            rbPerempuan.setSelected(true);
        }
        cbPendidikan.setSelectedItem(lastEducation);
        textFieldPhoneNumber.setText(phoneNumber);
        textFieldAddress.setText(address);
        textFieldLeadershipScore.setText(leadershipScore);
        textFieldKnowledgeScore.setText(knowledgeScore);
        textFieldTechnicalSkillScore.setText(technicalSkillScore);
        textFieldAdvancedSkillScore.setText(advancedSkillScore);
    }
    
